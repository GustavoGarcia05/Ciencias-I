package Logica;

public class NodoArreglo {
	
	private String padre;
	private int x;
	private int y;
	private char c;
	
	public NodoArreglo(){
		
	}
	
	public char getC() {
		return c;
	}

	public void setC(char c) {
		this.c = c;
	}

	public String getPadre() {
		return padre;
	}

	public void setPadre(String padre) {
		this.padre = padre;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	

}
